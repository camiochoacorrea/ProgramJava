    private void resolverImagenBoton(JButton boton, Silla silla) {
        String apellidoImg= "";
        
        switch (silla.getEstado()){
            case DISPONIBLE:
                apellidoImg=" sencilla.png" ;
                break;
            case OCUPADA:
                apellidoImg= " sencilla ocupada.jpg";
                break;
            case RESERVADA:
                apellidoImg= " sencilla ocupada.jpg";
                break;
        }
        
    
         ImageIcon imagen= new ImageIcon("/Cinema/"+ silla.getImagen()+ apellidoImg);
         boton.setIcon(imagen);
         
       
    }

    private Cliente buscarCliente(String documento) {
        return this.Clientes.get(documento);
        
    }

    private Venta buscarVenta(Integer noSilla) {
        
        for(Venta venta:Ventas){
            if(venta.getSilla().getNoSilla()== noSilla);
            return venta;
            
        }
        
        return null;
    }

    private void agregarVentaATabla(Venta venta) {
        
        DefaultTableModel modeloDeDatos= (DefaultTableModel) this.tlVentas.getModel();
        modeloDeDatos.addRow(venta.getDatosComoFila());
    }
