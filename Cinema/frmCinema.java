    public void seleccionarSilla (JButton btnSilla){
        Integer noSilla= Integer.valueOf(btnSilla.getActionCommand());
        Silla sillaSeleccionada= this.Sillas.get(noSilla);
        
        switch (sillaSeleccionada.getEstado()){
            case DISPONIBLE :
                reservarSilla (btnSilla, sillaSeleccionada);
                break;
            case RESERVADA:
                JOptionPane.showMessageDialog(this, "Esta silla está en proceso de venta");
            break;
            
            case OCUPADA:
                Venta venta= buscarVenta(noSilla);
                JOptionPane.showMessageDialog(this, "Esta silla está ocuapda por "+ venta.getCliente());

            default:
        }
    }
    
    
    public void reservarSilla (JButton btnSilla, Silla sillaSeleccionada){
        int diaSemana= getDiaSemana();
        double precio= sillaSeleccionada.calcularPrecio(diaSemana);
        if (precio==-1){
            JOptionPane.showMessageDialog(this, "Silla deshabilitada termporalmente");
            return;
        }
        
        
        this.sillaSeleccionada= sillaSeleccionada;
        this.botonSillaSeleccionada= btnSilla;
        
        sillaSeleccionada.cambiarEstado(Estado.RESERVADA);
        this.resolverImagenBoton(btnSilla, sillaSeleccionada);
                
        this.LabelSillaSeleccionada.setText(String.valueOf(sillaSeleccionada.getNoSilla()));
        this.LabelPrecio1.setText(String.valueOf(precio));
        btnSilla.setIcon(imgSillaOcupada);
        


           
}
        
        
        
        
    

